    {
        $conclusion=new Conclusion;
        $conclusion->conclusion=$request->conclusion;
        $conclusion->theme_id=$request->theme_id;
        $conclusion->observation=$request->observation;
        $conclusion->save();
        return redirect()->route('themes.index');
    }

